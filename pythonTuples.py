#learning about tuples
t = (1,2,3) #tuple

print(t)#1,2,3
print(len(t))#checks length of tuple

t = (1,'two',3)# can also hold many diffrent data types
print(t)

t = ('a','a','b')
print(t.count('a'))#counts how many times 'a' shows us
print(t.index('a'))#shows where 'a' first shows up)

#***IMPORTANT u can't change tuples the way u can change list (tuples are immutable)
